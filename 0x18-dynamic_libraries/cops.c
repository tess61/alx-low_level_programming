/**
 *add - perform adition
 *
 * Return: a + b
 */

int add(int a, int b)
{
return (a + b);
}

/**
 *sub - perform subtraction
 *
 * Return: a - b
 */

int sub(int a, int b)
{
return (a - b);
}

/**
 *mul - perform multiplication
 *
 * Return: a - b
 */

int mul(int a, int b)
{
return (a * b);
}

/**
 *div - perform div
 *
 * Return: a / b
 */

int div(int a, int b)
{
return (a / b);
}

/**
 *mod - perform mod
 *
 * Return: a - b
 */

int mod(int a, int b)
{
return (a % b);
}
